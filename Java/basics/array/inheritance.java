// Inheritance is a mechanism where a class (subclass) inherits fields and methods from another class (superclass).

// Extends - it lets one class inherit from another 
// Multi-Level inheritance 
// Single-level inheritance 
// 


class calc {
    public int add(int n1, int n2){
        return n1 + n2;
    }
    public int sub(int n1, int n2){
        return n1 - n2;
    }
    public int multi(int n1, int n2){
        return n1 * n2;
    }
    public int div(int n1, int n2){
        return n1 / n2;
    }
}

public class inheritance {
    public static void main(String []args){
        calc obj = new calc();
        int r1 = obj.add(10,10);
        int r2 = obj.sub(10,10);
        int r3 = obj.multi(10, 10);
        int r4 = obj.div(10,10);

        System.out.println(r1 + " " + r2 + " " + r3 + " " + r4);
    }
}
